export const getCitiesByState = (stateName: string): IndianCity[] => {
  return INDIAN_CITIES.filter(city => city.state === stateName);
};

export const getCityByName = (cityName: string, stateName?: string): IndianCity | undefined => {
  if (stateName) {
    return INDIAN_CITIES.find(city => city.name === cityName && city.state === stateName);
  }
  return INDIAN_CITIES.find(city => city.name === cityName);
};

